package com.suntendy.queue.tuiban.domain;

public class TuiBan {

	
	private String idnumber;//证件号码
	private String yuanyin;//原因
	private String jilutime;//记录时间
	private String jiluren;//记录人
	private String jiluip;//记录IP
	private String zt;//状态
	private String operation;//操作
	
	
	public String getIdnumber() {
		return idnumber;
	}
	public void setIdnumber(String idnumber) {
		this.idnumber = idnumber;
	}
	
	public String getYuanyin() {
		return yuanyin;
	}
	public void setYuanyin(String yuanyin) {
		this.yuanyin = yuanyin;
	}
	public String getJilutime() {
		return jilutime;
	}
	public void setJilutime(String jilutime) {
		this.jilutime = jilutime;
	}
	public String getJiluren() {
		return jiluren;
	}
	public void setJiluren(String jiluren) {
		this.jiluren = jiluren;
	}
	public String getJiluip() {
		return jiluip;
	}
	public void setJiluip(String jiluip) {
		this.jiluip = jiluip;
	}
	public String getZt() {
		return zt;
	}
	public void setZt(String zt) {
		this.zt = zt;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	
}
